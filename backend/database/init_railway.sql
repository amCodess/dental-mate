-- =========================================================
-- 2) Helpers: updated_at automático
-- =========================================================
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW."updated_at" = CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$;

ALTER FUNCTION set_updated_at() OWNER TO app_owner;

