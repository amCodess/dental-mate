-- =========================================================
-- 0) Roles base (ejecutar como superuser o rol con permisos)
-- =========================================================
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'app_owner') THEN
    CREATE ROLE app_owner NOINHERIT;
  END IF;

  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'app_user') THEN
    CREATE ROLE app_user NOINHERIT;
  END IF;

  -- Rol técnico para funciones SECURITY DEFINER que necesitan bypass RLS solo para membership.
  -- Requiere permisos para crear roles con BYPASSRLS (normalmente superuser).
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'app_rls') THEN
    CREATE ROLE app_rls NOINHERIT BYPASSRLS;
  END IF;
END$$;

-- Endurecer schema public (opcional pero recomendado en entornos reales)
REVOKE ALL ON SCHEMA public FROM PUBLIC;
GRANT USAGE ON SCHEMA public TO app_user;
GRANT USAGE, CREATE ON SCHEMA public TO app_owner;

