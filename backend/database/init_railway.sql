USING (
  app_current_user_id() IS NOT NULL
  AND (
    "Usuarios_Empresas"."id_usuario" = app_current_user_id()
    OR (
      app_has_tenant_context()
      AND app_user_can_write_in_tenant()
      AND "Usuarios_Empresas"."id_empresa" = app_current_tenant_id()
    )
  )
);

