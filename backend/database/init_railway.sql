-- 7.1 Variables de sesión
CREATE OR REPLACE FUNCTION app_current_user_id()
RETURNS int
LANGUAGE sql
STABLE
AS $$
  SELECT NULLIF(current_setting('app.user_id', true), '')::int
$$;

CREATE OR REPLACE FUNCTION app_current_tenant_id()
RETURNS int
LANGUAGE sql
STABLE
AS $$
  SELECT NULLIF(current_setting('app.tenant_id', true), '')::int
$$;

CREATE OR REPLACE FUNCTION app_has_tenant_context()
RETURNS boolean
LANGUAGE sql
STABLE
AS $$
  SELECT app_current_user_id() IS NOT NULL AND app_current_tenant_id() IS NOT NULL
$$;

ALTER FUNCTION app_current_user_id() OWNER TO app_owner;
ALTER FUNCTION app_current_tenant_id() OWNER TO app_owner;
ALTER FUNCTION app_has_tenant_context() OWNER TO app_owner;

-- 7.2 Membership: SECURITY DEFINER + row_security=off (evita recursión RLS)
CREATE OR REPLACE FUNCTION app_user_belongs_to_tenant()
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
SET row_security = off
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM "Usuarios_Empresas" ue
    WHERE ue."id_usuario" = app_current_user_id()
      AND ue."id_empresa" = app_current_tenant_id()
  )
$$;

CREATE OR REPLACE FUNCTION app_tenant_role()
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
SET row_security = off
AS $$
  SELECT ue."rol"::text
  FROM "Usuarios_Empresas" ue
  WHERE ue."id_usuario" = app_current_user_id()
    AND ue."id_empresa" = app_current_tenant_id()
$$;

CREATE OR REPLACE FUNCTION app_user_can_write_in_tenant()
RETURNS boolean
LANGUAGE sql
STABLE
AS $$
  SELECT app_user_belongs_to_tenant()
     AND lower(coalesce(app_tenant_role(), '')) IN ('owner','admin','staff','empleado','manager')
$$;

-- El owner de estas funciones debe poder bypass RLS
ALTER FUNCTION app_user_belongs_to_tenant() OWNER TO app_rls;
ALTER FUNCTION app_tenant_role() OWNER TO app_rls;
ALTER FUNCTION app_user_can_write_in_tenant() OWNER TO app_owner;

REVOKE ALL ON FUNCTION app_user_belongs_to_tenant() FROM PUBLIC;
REVOKE ALL ON FUNCTION app_tenant_role() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION app_user_belongs_to_tenant() TO app_user;
GRANT EXECUTE ON FUNCTION app_tenant_role() TO app_user;
GRANT EXECUTE ON FUNCTION app_user_can_write_in_tenant() TO app_user;

